function idx = takeInliers(x, center, tbins)
   [theta, ~] = cart2pol(x(:, 1) - center(1), x(:, 2) - center(2));%????[-pi,pi]???????????????? theta = atan2(x(:, 2) - center(2) , x(:, 1) - center(1)); 
    tmin = -pi; tmax = pi;
    tt = round((theta - tmin) / (tmax - tmin) * tbins + 0.5);%????????????[1 tbins]
    tt(tt < 1) = 1; tt(tt > tbins) = tbins;
    h = histc(tt, 1 : tbins);%h????????[1 tbins]??????????
    mark = zeros(tbins, 1);
    compSize = zeros(tbins, 1);
    nComps = 0;
    queue = zeros(tbins, 1);
    du = [-1, 1];
    for i = 1 : tbins
        if (h(i) > 0 && mark(i) == 0)%??????????i????????????????0????mark(i)??0
            nComps = nComps + 1;
            mark(i) = nComps;%??????nComps??????????
            front = 1; rear = 1;
            queue(front) = i;%????????????????????????????????
            while (front <= rear)
                u = queue(front);
                front = front + 1;
                for j = 1 : 2
                    v = u + du(j);
                    if (v == 0)
                        v = tbins;
                    end
                    if (v > tbins)
                        v = 1;
                    end
                    if (mark(v) == 0 && h(v) > 0)
                        rear = rear + 1;
                        queue(rear) = v;
                        mark(v) = nComps;%??????nComps??????????
                    end
                end
            end
            compSize(nComps) = sum(ismember(tt, find(mark == nComps)));%????????????????nComps??????????
        end
    end
    compSize(nComps + 1 : end) = [];
    maxCompSize = max(compSize);
    validComps = find(compSize >= maxCompSize * 0.1 & compSize > 10);%??????????????????????0.1????????????????????
    validBins = find(ismember(mark, validComps));%??????????
    idx = ismember(tt, validBins);%??????????
end